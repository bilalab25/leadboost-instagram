export type Language = 'en' | 'es';

export const getTranslation = (key: string, lang: Language = 'en') => {
  const keys = key.split('.');
  let value: any = translations[lang];
  
  for (const k of keys) {
    value = value?.[k];
  }
  
  return value || key;
};